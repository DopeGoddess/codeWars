const myArray = [];
let i = 5; // including 5 or interger in array
while (i >= 0){
    myArray.push(i);
    i--;// subracting a number
}
console.log(myArray);
//this is descending of an array including 5

const myArrays = [];
let a = 0; // include 0 
while(a < 5){ //does not include 5 
    myArrays.push(a);
    a++; // adding a number
}
console.log(myArrays);
//ascending order in an array